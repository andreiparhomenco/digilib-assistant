async def show_topic(update: Update, context: ContextTypes.DEFAULT_TYPE) -> int:
    """Show selected topic content."""
    query = update.callback_query
    await query.answer()
    
    # Extract topic ID from callback_data
    topic_id = query.data.replace("topic_", "")
    
    topic = EDUCATIONAL_TOPICS.get(topic_id)
    if not topic:
        await query.edit_message_text("‚ùå –¢–µ–º–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞")
        return 2
    
    # Get next topic for navigation
    next_topic_id = topic.get("next_topic")
    
    # Build navigation keyboard
    keyboard = []
    
    if next_topic_id:
        next_topic = EDUCATIONAL_TOPICS[next_topic_id]
        keyboard.append([
            InlineKeyboardButton(f"‚è≠Ô∏è {next_topic['title']}", callback_data=f"topic_{next_topic_id}"),
            InlineKeyboardButton("üîô –ö —Ç–µ–º–∞–º", callback_data="back_to_topics")
        ])
    else:
        # Last topic - suggest Creative Mode
        keyboard.append([
            InlineKeyboardButton("üí° –ü—Ä–∏–¥—É–º–∞—Ç—å –ø—Ä–æ–µ–∫—Ç", callback_data="mode_creative"),
            InlineKeyboardButton("üîô –ö —Ç–µ–º–∞–º", callback_data="back_to_topics")
        ])
    
    keyboard.append([InlineKeyboardButton("üè† –í –≥–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é", callback_data="back_to_main")])
    
    reply_markup = InlineKeyboardMarkup(keyboard)
    
    # Special message for completing all topics
    content = topic["content"]
    if not next_topic_id:
        content += "\n\nüéâ **–ü–æ–∑–¥—Ä–∞–≤–ª—è—é!** –¢—ã –∏–∑—É—á–∏–ª –≤—Å–µ –æ—Å–Ω–æ–≤—ã. –¢–µ–ø–µ—Ä—å –º–æ–∂–Ω–æ –ø—Ä–∏–¥—É–º–∞—Ç—å —Å–≤–æ–π –ø—Ä–æ–µ–∫—Ç ‚Üí"
    
    await query.edit_message_text(content, reply_markup=reply_markup, parse_mode='Markdown')
    
    return 3  # EDUCATIONAL_CONTENT state


